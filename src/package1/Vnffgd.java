public class Vnffgd {
    private Classifier[] classifier;
    private String description;
    private String id;
    private String name;
    private Rsp[] rsp;
    private String shortName;
    private String vendor;
    private String version;


    public Classifier[] getClassifier() { return classifier; }

    public void setClassifier(Classifier[] value) { this.classifier = value; }

  
    public String getDescription() { return description; }
 
    public void setDescription(String value) { this.description = value; }

    
    public String getID() { return id; }
   
    public void setID(String value) { this.id = value; }

   
    public String getName() { return name; }

    public void setName(String value) { this.name = value; }

  
    public Rsp[] getRsp() { return rsp; }
  
    public void setRsp(Rsp[] value) { this.rsp = value; }

 
    public String getShortName() { return shortName; }
 
    public void setShortName(String value) { this.shortName = value; }


    public String getVendor() { return vendor; }

    public void setVendor(String value) { this.vendor = value; }

  
    public String getVersion() { return version; }
  
    public void setVersion(String value) { this.version = value; }
}